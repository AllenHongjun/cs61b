public class PrintAbsoluteValue {

    /**
     * ideal 使用 有哪些方法
     * 能够提高敲代码的效率
     * 比如 main 直接一个代码段 等等
     * @param args
     */
    public static void main(String[] args) {
        int x = -5;

        if (x < 0)
            System.out.println("I should negate X");
            x = -x;


        System.out.println(x);

    }
}
